package Loops;

// Output the first 10 numbers of the sequence 0, -5,-10,-15.
public class Task5 {
    public static void main(String[] args) {
        int sum = 0;
        System.out.println(sum);
        for (int i = 1; i < 10; i++) {
            sum += -5;
            System.out.println(sum);
        }

    }
}
